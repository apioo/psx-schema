class PopoClass implements AdditionalPropertiesInterface
{
    protected $foo;
    protected $bar;

    /**
     * @return mixed
     */
    public function getFoo()
    {
        return $this->foo;
    }

    /**
     * @param mixed $foo
     */
    public function setFoo($foo)
    {
        $this->foo = $foo;
    }

    /**
     * @return mixed
     */
    public function getBar()
    {
        return $this->bar;
    }

    public function setProperty($name, $value)
    {
        $this->bar = $value;
    }
}
